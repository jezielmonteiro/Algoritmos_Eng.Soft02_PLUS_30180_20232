#Fazer um programa que imprima a média aritmética dos números 8,9 e 7. A média dos números 4, 5 e 6. A soma das duas médias. A média das medias.


media1 = (8 + 9 + 7) / 3
print("A média aritmética dos números 8, 9 e 7 é: ", media1)

media2 = (4 + 5 + 6) / 3
print("A média aritmética dos números 4, 5 e 6 é: ", media2)

medias_soma = media1 + media2
print("A soma das duas médias é: ", medias_soma)

medias_média = (media1 + media2) / 2
print("A média das medias é: ", medias_média)





